export const getTemperatureColor = (temperature: number): string => {
  if (temperature > 45) return '#000002'
  else if (temperature > 40) return '#840000'
  else if (temperature > 35) return '#993400'
  else if (temperature > 30) return '#FA0300'
  else if (temperature > 26) return '#FC6900'
  else if (temperature > 22) return '#FFFF00'
  else if (temperature > 20) return '#FFFD99'
  else if (temperature > 17) return '#00FF01'
  else if (temperature > 15) return '#D1FCCE'
  else if (temperature > 14) return '#CCFFFF'
  else if (temperature > 12) return '#99CDFF'
  else if (temperature > 10) return '#00CDFF'
  else if (temperature > 5) return '#9CEBFF'
  else if (temperature >= 0) return '#00DCFF'
  else if (temperature > -5) return '#009DD8'
  else if (temperature > -10) return '#057BDB'
  else if (temperature > -15) return '#0059A1'
  else return '#012E52'
}
